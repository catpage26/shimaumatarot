document.addEventListener("DOMContentLoaded", () => {
  const mainCatArea = document.getElementById("main-category-area");
  if (!mainCatArea) return; // 鑑定ページ以外では実行しない

  const subCatArea = document.getElementById("sub-category-area");
  const subCatButtons = document.getElementById("sub-cat-buttons");
  const subCatTitle = document.getElementById("sub-cat-title");
  const drawSection = document.getElementById("draw-section");
  const drawBtn = document.getElementById("draw-btn");
  const cardArea = document.getElementById("card-area");
  const readingArea = document.getElementById("reading-area");
  const resetBtn = document.getElementById("reset-btn");
  const backToMainBtn = document.getElementById("back-to-main");
  const selectedLabel = document.getElementById("selected-label");

  let selectedSubId = null;
  let isAnimating = false;

  // --- 大カテゴリボタンを動的生成 ---
  const mainGrid = document.getElementById("main-cat-grid");
  for (const [catId, cat] of Object.entries(CATEGORIES)) {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = "main-cat-btn";
    btn.dataset.category = catId;
    btn.innerHTML = `<span class="main-cat-emoji">${cat.emoji}</span><span class="main-cat-label">${cat.label}</span>`;
    btn.addEventListener("click", () => showSubCategories(catId));
    mainGrid.appendChild(btn);
  }

  // --- 大カテゴリ選択 → 中カテゴリ表示 ---
  function showSubCategories(catId) {
    const cat = CATEGORIES[catId];
    subCatTitle.textContent = `${cat.emoji} ${cat.label}`;
    subCatButtons.innerHTML = "";

    for (const sub of cat.subs) {
      const btn = document.createElement("button");
      btn.type = "button";
      btn.className = "sub-cat-btn";
      btn.dataset.subId = sub.id;
      btn.textContent = sub.label;
      btn.addEventListener("click", () => selectSubCategory(sub.id, sub.label, cat.emoji));
      subCatButtons.appendChild(btn);
    }

    mainCatArea.classList.add("hidden");
    subCatArea.classList.remove("hidden");
    drawSection.classList.add("hidden");
    cardArea.classList.add("hidden");
    readingArea.classList.add("hidden");
    resetBtn.classList.add("hidden");
  }

  // --- 中カテゴリ選択 → カードを引くボタン表示 ---
  function selectSubCategory(subId, label, emoji) {
    selectedSubId = subId;
    // 選択状態のハイライト
    subCatButtons.querySelectorAll(".sub-cat-btn").forEach(b => b.classList.remove("selected"));
    subCatButtons.querySelector(`[data-sub-id="${subId}"]`).classList.add("selected");
    // カードを引くセクション表示
    selectedLabel.textContent = `${emoji} ${label}`;
    drawSection.classList.remove("hidden");
    drawSection.scrollIntoView({ behavior: "smooth", block: "nearest" });
  }

  // --- 戻るボタン ---
  backToMainBtn.addEventListener("click", () => {
    subCatArea.classList.add("hidden");
    drawSection.classList.add("hidden");
    cardArea.classList.add("hidden");
    readingArea.classList.add("hidden");
    resetBtn.classList.add("hidden");
    mainCatArea.classList.remove("hidden");
    selectedSubId = null;
  });

  // --- カードを引く ---
  drawBtn.addEventListener("click", () => {
    if (isAnimating || !selectedSubId) return;

    isAnimating = true;
    drawBtn.disabled = true;
    drawBtn.textContent = "カードを引いてるよ…";

    cardArea.classList.remove("hidden");
    readingArea.classList.add("hidden");
    resetBtn.classList.add("hidden");

    const drawnCard = drawCard();

    showShuffleAnimation(() => {
      showCard(drawnCard, () => {
        const reading = generateReading(selectedSubId, drawnCard);
        showReading(reading);
        // アフィリエイト枠を表示（カテゴリ別に広告を出し分け）
        const affArea = document.getElementById("affiliate-area");
        if (affArea) {
          updateAffiliateAd(selectedSubId);
          affArea.classList.remove("hidden");
        }
        isAnimating = false;
        drawBtn.disabled = false;
        drawBtn.textContent = "🔮 カードを引く";
      });
    });
  });

  // --- もう一度引く ---
  resetBtn.addEventListener("click", () => {
    cardArea.classList.add("hidden");
    readingArea.classList.add("hidden");
    resetBtn.classList.add("hidden");
    drawSection.classList.add("hidden");
    subCatArea.classList.add("hidden");
    mainCatArea.classList.remove("hidden");
    // アフィリエイト枠を隠す
    const affArea = document.getElementById("affiliate-area");
    if (affArea) affArea.classList.add("hidden");
    selectedSubId = null;
  });

  // --- アニメーション系 ---
  function showShuffleAnimation(callback) {
    cardArea.innerHTML = "";
    const shuffleContainer = document.createElement("div");
    shuffleContainer.className = "shuffle-container";
    for (let i = 0; i < 5; i++) {
      const c = document.createElement("div");
      c.className = "shuffle-card";
      c.style.animationDelay = `${i * 0.1}s`;
      shuffleContainer.appendChild(c);
    }
    const shuffleText = document.createElement("p");
    shuffleText.className = "shuffle-text";
    shuffleText.textContent = "78枚のカードをシャッフルしてるよ…";
    cardArea.appendChild(shuffleContainer);
    cardArea.appendChild(shuffleText);
    setTimeout(callback, 2000);
  }

  function showCard(drawnCard, callback) {
    const { card, isReversed } = drawnCard;
    cardArea.innerHTML = "";
    const cardEl = document.createElement("div");
    cardEl.className = "tarot-card";
    const cardInner = document.createElement("div");
    cardInner.className = "card-inner";
    const cardBack = document.createElement("div");
    cardBack.className = "card-back";
    cardBack.innerHTML = `<div class="card-back-pattern">🌙 ✨ 🌙</div>`;
    const cardFront = document.createElement("div");
    cardFront.className = `card-front ${isReversed ? "reversed" : ""}`;
    const topLabel = card.number !== undefined ? card.number : card.emoji;
    cardFront.innerHTML = `
      <div class="card-number">${topLabel}</div>
      <div class="card-emoji">${card.emoji}</div>
      <div class="card-name">${card.name}</div>
      <div class="card-position">${isReversed ? "逆位置" : "正位置"}</div>
    `;
    cardInner.appendChild(cardBack);
    cardInner.appendChild(cardFront);
    cardEl.appendChild(cardInner);
    cardArea.appendChild(cardEl);
    setTimeout(() => cardEl.classList.add("flipped"), 500);
    setTimeout(callback, 1800);
  }

  function showReading(reading) {
    readingArea.classList.remove("hidden");
    readingArea.innerHTML = "";
    const header = document.createElement("h2");
    header.className = "reading-header";
    header.innerHTML = `${reading.card.emoji}${reading.card.name}（${reading.positionLabel}）からのメッセージ`;
    const textContainer = document.createElement("div");
    textContainer.className = "reading-text";
    readingArea.appendChild(header);
    readingArea.appendChild(textContainer);
    typeText(textContainer, reading.text, 0);
  }

  function typeText(el, text, idx) {
    if (idx < text.length) {
      const char = text[idx];
      if (char === "\n") {
        el.appendChild(document.createElement("br"));
      } else {
        el.appendChild(document.createTextNode(char));
      }
      const delay = (char === "。" || char === "、" || char === "…") ? 80 : 25;
      setTimeout(() => typeText(el, text, idx + 1), delay);
    } else {
      resetBtn.classList.remove("hidden");
      readingArea.scrollIntoView({ behavior: "smooth", block: "end" });
    }
  }
});
