// しまうまタロット風の鑑定文を生成（中カテゴリIDで呼び出す）
// nameEnからcard_readings.jsのキーに変換
function getCardId(card) {
  if (card.id) return card.id;
  const nameEn = card.nameEn || "";
  // メジャーアルカナ
  const majorMap = {
    "The Fool": "fool", "The Magician": "magician", "The High Priestess": "high_priestess",
    "The Empress": "empress", "The Emperor": "emperor", "The Hierophant": "hierophant",
    "The Lovers": "lovers", "The Chariot": "chariot", "Strength": "strength",
    "The Hermit": "hermit", "Wheel of Fortune": "wheel_of_fortune", "Justice": "justice",
    "The Hanged Man": "hanged_man", "Death": "death", "Temperance": "temperance",
    "The Devil": "devil", "The Tower": "tower", "The Star": "star",
    "The Moon": "moon", "The Sun": "sun", "Judgement": "judgement", "The World": "world"
  };
  if (majorMap[nameEn]) return majorMap[nameEn];
  // マイナーアルカナ: "Ace of Wands" → "wands_ace", "Two of Cups" → "cups_two"
  const minorMatch = nameEn.match(/^(Ace|Two|Three|Four|Five|Six|Seven|Eight|Nine|Ten|Page|Knight|Queen|King) of (Wands|Cups|Swords|Pentacles)$/);
  if (minorMatch) {
    const rankMap = { Ace:"ace", Two:"two", Three:"three", Four:"four", Five:"five",
      Six:"six", Seven:"seven", Eight:"eight", Nine:"nine", Ten:"ten",
      Page:"page", Knight:"knight", Queen:"queen", King:"king" };
    return minorMatch[2].toLowerCase() + "_" + rankMap[minorMatch[1]];
  }
  return "";
}

