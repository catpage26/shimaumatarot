function generateReading(subCategoryId, drawnCard) {
  const { card, isReversed } = drawnCard;
  const position = isReversed ? "reversed" : "upright";
  const positionLabel = isReversed ? "逆位置" : "正位置";
  const data = card[position];

  const parentId = SUB_TO_PARENT[subCategoryId] || "energy";
  const tpl = CATEGORY_TEMPLATES[parentId];
  const subLabel = SUB_LABELS[subCategoryId] || "あなたのこと";
  const cardId = getCardId(card);

  // ---- サブカテゴリ別鑑定文を取得 ----
  let readingText = "";
  if (typeof CARD_SUBCATEGORY_READINGS !== "undefined" &&
      CARD_SUBCATEGORY_READINGS[subCategoryId] &&
      CARD_SUBCATEGORY_READINGS[subCategoryId][cardId] &&
      CARD_SUBCATEGORY_READINGS[subCategoryId][cardId][position]) {
    readingText = CARD_SUBCATEGORY_READINGS[subCategoryId][cardId][position];
  } else {
    // フォールバック: カード基本データ（本来ここには来ない）
    console.warn("[鑑定] フォールバック発生:", { subCategoryId, cardId, position,
      dataLoaded: typeof CARD_SUBCATEGORY_READINGS !== "undefined",
      subExists: typeof CARD_SUBCATEGORY_READINGS !== "undefined" && !!CARD_SUBCATEGORY_READINGS[subCategoryId],
      cardExists: typeof CARD_SUBCATEGORY_READINGS !== "undefined" && CARD_SUBCATEGORY_READINGS[subCategoryId] && !!CARD_SUBCATEGORY_READINGS[subCategoryId][cardId]
    });
    readingText = data.meaning + "\n" + data.advice;
  }

  // ---- 冒頭（テーマの提示） ----
  const openings = [
    `「${subLabel}」について、カードに聞いてみました。`,
    `「${subLabel}」について、宇宙からのメッセージを受け取りました。`,
    `気持ち、しっかりカードに届いていますよ。「${subLabel}」について見ていきますね。`,
    `「${subLabel}」…気になりますよね。カードからのメッセージをお伝えしますね。`
  ];

  // ---- カード紹介 ----
  const cardIntros = [
    `出てきたカードは「${card.emoji}${card.name}」の${positionLabel}です。`,
    `あなたに届いたカードは「${card.emoji}${card.name}」の${positionLabel}です。`,
    `${card.emoji}「${card.name}」が${positionLabel}で出ました。`
  ];

  const keywordSection = `\nキーワードは【${data.keyword}】。`;
  const closing = randomChoice(tpl.closings);

  // ---- 組み立て ----
  const text = [
    randomChoice(openings),
    "",
    `${randomChoice(cardIntros)}${keywordSection}`,
    "",
    readingText,
    closing
  ].join("\n");

  return {
    text: text,
    card: card,
    isReversed: isReversed,
    positionLabel: positionLabel
  };
}

function truncate(str, len) {
  return str.length > len ? str.substring(0, len) + "…" : str;
}

function randomChoice(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

